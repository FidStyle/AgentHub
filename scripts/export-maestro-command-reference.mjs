#!/usr/bin/env node

import fs from 'node:fs'
import path from 'node:path'

const repoRoot = process.cwd()
const catalogPath = path.join(repoRoot, '.claude/skills/maestro-help/index/catalog.json')
const commandsDir = path.join(repoRoot, '.claude/commands')
const outputPath = path.join(repoRoot, 'research/maestro-command-reference.md')

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'))
}

function parseFrontmatter(markdown) {
  const normalized = markdown.replace(/\r\n/g, '\n')
  if (!normalized.startsWith('---\n')) return {}
  const end = normalized.indexOf('\n---', 4)
  if (end === -1) return {}

  const block = normalized.slice(4, end).trim()
  const result = {}
  let currentKey = null

  for (const rawLine of block.split('\n')) {
    const line = rawLine.replace(/\r$/, '')
    const keyValue = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/)
    const listItem = line.match(/^\s+-\s*(.*)$/)

    if (keyValue) {
      currentKey = keyValue[1]
      const value = keyValue[2].trim()
      result[currentKey] = value === '' ? [] : value.replace(/^["']|["']$/g, '')
      continue
    }

    if (listItem && currentKey) {
      if (!Array.isArray(result[currentKey])) result[currentKey] = []
      result[currentKey].push(listItem[1].trim().replace(/^["']|["']$/g, ''))
    }
  }

  return result
}

function commandFrontmatterByName() {
  const entries = new Map()
  for (const file of fs.readdirSync(commandsDir)) {
    if (!file.endsWith('.md')) continue
    const fullPath = path.join(commandsDir, file)
    const frontmatter = parseFrontmatter(fs.readFileSync(fullPath, 'utf8'))
    if (frontmatter.name) {
      entries.set(frontmatter.name, {
        ...frontmatter,
        file: path.relative(repoRoot, fullPath),
      })
    }
  }
  return entries
}

function table(rows, columns) {
  const header = `| ${columns.map((c) => c.label).join(' |')} |`
  const separator = `| ${columns.map(() => '---').join(' |')} |`
  const body = rows.map((row) => {
    const cells = columns.map((column) => {
      const value = column.value(row) ?? ''
      return String(value).replace(/\n/g, '<br>').replace(/\|/g, '\\|')
    })
    return `| ${cells.join(' |')} |`
  })
  return [header, separator, ...body].join('\n')
}

const catalog = readJson(catalogPath)
const frontmatter = commandFrontmatterByName()

const enrichedCommands = catalog.commands.map((command) => {
  const fm = frontmatter.get(command.name) ?? {}
  return {
    ...command,
    argumentHint: fm['argument-hint'] ?? '',
    allowedTools: Array.isArray(fm['allowed-tools']) ? fm['allowed-tools'].join(', ') : '',
    file: fm.file ?? command.source,
  }
})

const commandsByCategory = new Map()
for (const command of enrichedCommands) {
  if (!commandsByCategory.has(command.category)) commandsByCategory.set(command.category, [])
  commandsByCategory.get(command.category).push(command)
}

const focusedCategories = ['ralph', 'pipeline', 'quality', 'milestone', 'core']
const workflowPaths = catalog.workflows?.paths ?? {}
const mainPipeline = catalog.workflows?.main_pipeline ?? {}

const lines = []
lines.push('# Maestro Command Reference')
lines.push('')
lines.push('> Generated by `node scripts/export-maestro-command-reference.mjs`.')
lines.push('> This is a compact Codex-facing index extracted from `.claude/commands/*` frontmatter and `.claude/skills/maestro-help/index/catalog.json`.')
lines.push('')
lines.push('## Command Rule')
lines.push('')
lines.push('- Prefer `argument-hint` for invocation shape; do not infer parameters from memory.')
lines.push('- For adaptive lifecycle work, start with `/maestro-ralph "<intent>" -y`, then use `/maestro-ralph status` and `/maestro-ralph-execute` / `/maestro-ralph continue` according to session state.')
lines.push('- For a known single stage, use the stage command directly, for example `/maestro-execute`, `/maestro-verify`, or `/quality-review`.')
lines.push('- `.workflow/.maestro/*/status.json` is execution state, not product completion evidence.')
lines.push('')
lines.push('## Ralph Entry Points')
lines.push('')
lines.push(table(
  (commandsByCategory.get('ralph') ?? []),
  [
    { label: 'Command', value: (row) => row.command },
    { label: 'Use', value: (row) => row.description },
    { label: 'Argument Hint', value: (row) => row.argumentHint || '(none)' },
    { label: 'File', value: (row) => row.file },
  ],
))
lines.push('')
lines.push('## Pipeline Commands')
lines.push('')
lines.push(table(
  (commandsByCategory.get('pipeline') ?? []),
  [
    { label: 'Command', value: (row) => row.command },
    { label: 'Use', value: (row) => row.description },
    { label: 'Argument Hint', value: (row) => row.argumentHint || '(none)' },
  ],
))
lines.push('')
lines.push('## Quality And Closure')
lines.push('')
lines.push(table(
  [...(commandsByCategory.get('quality') ?? []), ...(commandsByCategory.get('milestone') ?? [])],
  [
    { label: 'Command', value: (row) => row.command },
    { label: 'Use', value: (row) => row.description },
    { label: 'Argument Hint', value: (row) => row.argumentHint || '(none)' },
  ],
))
lines.push('')
lines.push('## Core Commands')
lines.push('')
lines.push(table(
  focusedCategories.flatMap((category) => commandsByCategory.get(category) ?? []).filter((row) => row.category === 'core'),
  [
    { label: 'Command', value: (row) => row.command },
    { label: 'Use', value: (row) => row.description },
    { label: 'Argument Hint', value: (row) => row.argumentHint || '(none)' },
  ],
))
lines.push('')
lines.push('## Workflow Paths')
lines.push('')
for (const [key, value] of Object.entries(workflowPaths)) {
  lines.push(`- \`${key}\`: ${value}`)
}
lines.push('')
lines.push('## Main Pipeline')
lines.push('')
lines.push(`- Phases: ${(mainPipeline.phases ?? []).map((item) => `\`${item}\``).join(' -> ')}`)
lines.push(`- Quality: ${(mainPipeline.quality ?? []).map((item) => `\`${item}\``).join(' -> ')}`)
lines.push(`- Close: ${(mainPipeline.close ?? []).map((item) => `\`${item}\``).join(' -> ')}`)
lines.push('')
lines.push('## Essential Commands')
lines.push('')
for (const command of catalog.essential_commands ?? []) {
  const entry = enrichedCommands.find((item) => item.command === command)
  lines.push(`- \`${command}\`${entry?.argumentHint ? ` ${entry.argumentHint}` : ''} — ${entry?.description ?? ''}`)
}
lines.push('')
lines.push('## Guide Files')
lines.push('')
for (const guide of catalog.guide_files ?? []) {
  lines.push(`- \`${guide.file}\` — ${guide.title}: ${guide.description}`)
}
lines.push('')

fs.writeFileSync(outputPath, `${lines.join('\n')}\n`)
console.log(`Wrote ${path.relative(repoRoot, outputPath)}`)
